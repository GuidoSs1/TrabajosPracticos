/** \brief Devuelve la suma de los operandos ingresados
 *
 * \param float Pide el primer operando
 * \param float Pide el segundo operando
 * \return retorna la suma de ambos operandos
 *
 */

float fSuma(float, float);

/** \brief Devuelve la resta de los operandos ingresados
 *
 * \param double Pide el primer operando
 * \param double Pide el segundo operando
 * \return retorna la resta de ambos operandos
 *
 */

float fResta(float, float);

/** \brief Devuelve la multiplicacion de los operandos ingresados
 *
 * \param float Pide el primer operando
 * \param float Pide el segundo operando
 * \return retorna el producto de ambos operandos
 *
 */

float fMultiplicacion(float, float);

/** \brief Devulve la division de los operandos ingresados
 *
 * \param float Pide el primer operando
 * \param float Pide el segundo operando
 * \return retorna el resto de la division entre ambos operandos
 *
 */

float fDivision(float, float);

/** \brief Devuelve el factorial del primer operando ingresado
 *
 * \param float Pide el primer operando ingresado
 * \return retorna el factorial del operando
 *
 */

double factorialUno(float);

/** \brief Devuelve el factorial del segundo operando ingresado
 *
 * \param float Pide el segundo operando ingresado
 * \return retorna el factorial del operando
 *
 */

double factorialDos(float);
